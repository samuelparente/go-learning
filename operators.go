package main

import "fmt"

func main() {
	x := 10
	y := 20

	fmt.Println("Addition:", x+y)
	fmt.Println("Subtraction:", x-y)
	fmt.Println("Multiplication:", x*y)
	fmt.Println("Division:", x/y)
	fmt.Println("Modulus:", x%y)
}
